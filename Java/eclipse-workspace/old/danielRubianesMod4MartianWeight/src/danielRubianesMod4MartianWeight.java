// import the classes necessary for scanner objects
import java.io.*;
import java.util.*;

public class danielRubianesMod4MartianWeight
{
    public static void main(String args[])
    {
    	// create the scanner object
    	Scanner userInput = new Scanner(System.in);
    	// ask for name
    	System.out.println("Enter your name:");
    	String name = userInput.next();
    	// ask for earth weight
    	System.out.println("Enter your weight on earth (in lbs):");
    	double earthWeight = userInput.nextDouble();
    	// display earth and mars weight
    	System.out.println(name + "'s weight on Earth is " + earthWeight + "lbs and weight on Mars is " + earthWeight*.38 + "lbs");
    }
}

