public class danielRubianesMod2SumAverage
{
    public static void main(String args[])
    {
    	int num1 = 475;
    	int num2 = 821;
    	int num3 = 369;
    	int num4 = 562;
    	int sum = num1 + num2 + num3 + num4;
    	int avg = sum / 4;
    	System.out.println("Thank you for entering your four numbers!");
    	System.out.println("The sum of the four numbers is: " + sum);
    	System.out.println("The average of the four number is: " + avg);
    	
    }
}

